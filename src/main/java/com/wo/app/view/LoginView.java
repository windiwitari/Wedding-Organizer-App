    private void comboBahasaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_comboBahasaActionPerformed
        // TODO add your handling code here:
        String selectedLanguage = (String) comboBahasa.getSelectedItem();

    if (selectedLanguage.equals("English")) {
        LocalizationManager.setLocale(new Locale("en", "US"));
    } else { // Default ke Indonesia
        LocalizationManager.setLocale(new Locale("id", "ID"));
    }

    // Setelah bahasa diubah, panggil method untuk me-refresh semua teks di UI
    updateUITexts();
    }//GEN-LAST:event_comboBahasaActionPerformed

